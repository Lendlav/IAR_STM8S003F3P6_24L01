#define FONT_BASE_ADDRESS       0x4000  //адрес размещения шрифтов

#define BUFF_SIZE       512

#define column_start  0
#define column_end    127
#define page_start    0
#define page_end      3

//----------------------------------------------------------------------------------
u8  lcd_buff[BUFF_SIZE];
u16 lcd_buff_idx=0;
u8  x_cur=0, y_cur=0;

const u8 comm[]={ 
   0xAE,             //Display Off
   0xD5,0x80,
   
   0xA8,0x1F,        //Set Multiplex Ratio     1F - 128x32, 3F - 128x64
            
   0xD3,0x00,        //Set Display Offset
   0x40,
   
   0x8D,0x14,        // Charge Pump set:   0x10 = VCC Supplied Externally  
                     // 0x14 = VCC Generated by Internal
   
   //[1:0] = 00b, Horizontal Addressing Mode
   //[1:0] = 01b, Vertical Addressing Mode
   //[1:0] = 10b, Page Addressing Mode (RESET)
   //[1:0] = 11b, Invalid
   0x20,0x01,        //Set Memory Addressing Mode 
   
   0xA0,             //Set Segment Re-map  A0/A1
   0xC0,             //Vertical flip    C0/C8
   
   0xDA,0x02,        //Set COM Pins Hardware Configuration  0x10 - 128x64, 0x02 - 128x32
   
   0x81,0x00,        //Set Contrast Control 
   
   0xD9,0xF1,        //Set Pre-Charge Period:  0x22 = VCC Supplied Externally  \
                     //0xF1 = VCC Generated by Internal             
      
   0xDB,0x40,        //Set VCOMH Deselect Level
      
   0xA4,             //Set Entire Display On/Off
   0xA6,             //Set NORMAL/INVERT Display   A6/A7
   
   0xAF,             //Display On
   
   0xFF
};
//----------------------------------------------------------------------------------
void LCD_Mode(u8 mode)
{
  LCD_command(0x20);
  LCD_command(mode);
}
//----------------------------------------------------------------------------------
void LCD_Flip(u8 flip)
{
  if(flip)
  {
    LCD_command(0xA0);
    LCD_command(0xC0);
  }
  else
  {
    LCD_command(0xA1);
    LCD_command(0xC8);
  }
}
//----------------------------------------------------------------------------------
void LCD_Init(void)
{
  u8 ix = 0;
  while(comm[ix]!=0xFF)
  {
     LCD_command(comm[ix]);
     ix++;
  }
}
//----------------------------------------------------------------------------------
void LCD_Contrast(u8 contrast)
{
   LCD_command(0x81);
   LCD_command(contrast);
}
//----------------------------------------------------------------------------------
void LCD_On(void)
{
   LCD_command(0xAF);
}
//----------------------------------------------------------------------------------
void LCD_Off(void)
{
   LCD_command(0xAE);
}
//----------------------------------------------------------------------------------
void LCD_Clear(void)
{
   u16 ix;
   for(ix=0;ix<BUFF_SIZE;ix++)
   {
      lcd_buff[ix]=0x00;
   }
   x_cur=0;
   y_cur=0;
}
//----------------------------------------------------------------------------------

void LCD_Update(void)
{
   u16 ix = 0;

   LCD_command(0x21);             // SSD1306_COLUMNADDR
   LCD_command(column_start);     // column start
   LCD_command(column_end);       // column end
   LCD_command(0x22);             // SSD1306_PAGEADDR
   LCD_command(page_start);       // page start
   LCD_command(page_end);         // page end (4 pages for 32 rows OLED)
   
   disableInterrupts();
   
   I2C->CR2 |= I2C_CR2_START;   ///I2C_GenerateSTART(ENABLE);
   while (!I2C_CheckEvent(I2C_EVENT_MASTER_MODE_SELECT));
   I2C_Send7bitAddress(SLAVE_ADDRESS, I2C_DIRECTION_TX);
   while (!I2C_CheckEvent(I2C_EVENT_MASTER_TRANSMITTER_MODE_SELECTED));
   I2C->DR = DATA_MODE;         //I2C_SendData(DATA_MODE);
   while (!I2C_CheckEvent(I2C_EVENT_MASTER_BYTE_TRANSMITTING));  
   
   for(ix=0;ix<BUFF_SIZE;ix++)
   {
     I2C->DR = lcd_buff[ix];    //I2C_SendData(lcd_buff[ix]);
     while (!I2C_CheckEvent(I2C_EVENT_MASTER_BYTE_TRANSMITTING));
   } 
   I2C->CR2 |= I2C_CR2_STOP;    //I2C_GenerateSTOP(ENABLE);
   
   enableInterrupts();
}
//----------------------------------------------------------------------------------
void LCD_Chr(u8 ch, u8 invers)
{
   u8 i;   
   lcd_buff_idx=y_cur*128+x_cur*6;    
   if ((ch>=0x20)&&(ch<=0x7F)) ch-=32;
   else if (ch>=0xC0) ch-=96;
   else ch = 95;   
   for (i=0;i<5;i++)
   {
     if(invers)
      lcd_buff[lcd_buff_idx++] = ~(font[ch][i]);
     else
       lcd_buff[lcd_buff_idx++] = font[ch][i];
   }
   if(invers)                           //depict space between symbols
     lcd_buff[lcd_buff_idx++] = 0xFF; 
   else
     lcd_buff[lcd_buff_idx++] = 0x00; 
    
   x_cur++;
   if (x_cur>20)
   {
      x_cur=0;
      y_cur++;
      if (y_cur>3)
      {
         y_cur=0;
      }
   }
}
//----------------------------------------------------------------------------------
void LCD_2xChr(u8 ch)
{
   u8	i, c;
   u8	b1, b2;  
   u16	tmpIdx;
   
   lcd_buff_idx = (y_cur*128 + x_cur*6)+1;
   tmpIdx       = lcd_buff_idx - 128;
   if ((ch>=0x20)&&(ch<=0x7F)) ch-=32;
   else if (ch>=0xC0) ch-= 96;
   else               ch = 95;
   
   for ( i = 0; i < 5; i++ ){
      c = font[ch][i];//<<1;
      b1=(c&0x01)*3;
      b1|=(c&0x02)*6;
      b1|=(c&0x04)*12;
      b1|=(c&0x08)*24;
      
      c>>=4;
      b2=(c&0x01)*3;
      b2|=(c&0x02)*6;
      b2|=(c&0x04)*12;
      b2|=(c&0x08)*24;
      
      lcd_buff[tmpIdx++]=b1;
      lcd_buff[tmpIdx++]=b1;
      lcd_buff[tmpIdx+126]=b2;
      lcd_buff[tmpIdx+127]=b2;
   }
   lcd_buff_idx=(lcd_buff_idx+11)%BUFF_SIZE;
   lcd_buff[lcd_buff_idx]=0x00;    
   if(lcd_buff_idx==(BUFF_SIZE-1)) lcd_buff_idx=1;
   else lcd_buff_idx++;
   x_cur=x_cur+2;
   if (x_cur>19){
      x_cur=0;
      y_cur=y_cur+2;
      if (y_cur>3){
         y_cur=0;
      }
   }
}
//----------------------------------------------------------------------------------
void LCD_FStr(const u8 *dataPtr,u8 x,u8 y, u8 invers)
{
   LCD_GotoXY(x,y);
   while(*dataPtr!=0)
   {
      LCD_Chr(*dataPtr, invers);
      dataPtr++;
   }
}
//----------------------------------------------------------------------------------
void LCD_2xFStr(const u8 *dataPtr,u8 x,u8 y)
{
   LCD_GotoXY(x,y);
   while(*dataPtr!=0)
   {
      LCD_2xChr(*dataPtr);
      dataPtr++;
   }
}
//----------------------------------------------------------------------------------
void LCD_GotoXY(u8 x,u8 y)
{
    if((x<21)&&(y<4))
    {
      x_cur=x;
      y_cur=y;
    }
}
