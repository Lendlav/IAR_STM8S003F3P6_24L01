void LCD_Batt(int val)
{ 
  if(val<0)val = 0;
  u8 bat_arr[] = 
  { 
    0x3C, 0x3C, 0xFF, 0x81, 0x81, 0x81, 
    0x81, 0x81, 0x81, 0x81, 0x81, 0x81, 
    0x81, 0x81, 0x81, 0x81, 0x81, 0x81, 
    0x81, 0xFF 
  };
  
  u8 cnt_val = 17;
  while(val>0)
  {      
    bat_arr[cnt_val--]= 0xBD; 
    val -=15;
  }
  lcd_buff_idx=483;
  for(u8 i=0;i<20;i++)
  {
    lcd_buff[lcd_buff_idx++]=bat_arr[i];
  }
}
//----------------------------------------------------------------------------------

void  LCD_command(u8 cmd)
{
start: 
  u8 count_wdt =0;
  I2C->CR2 |= I2C_CR2_START; 
  while (!I2C_CheckEvent(I2C_EVENT_MASTER_MODE_SELECT))
  {
    if(count_wdt++>10)goto start;
  }
  I2C_Send7bitAddress(SLAVE_ADDRESS, I2C_DIRECTION_TX);
  
  count_wdt = 0;
  while (!I2C_CheckEvent(I2C_EVENT_MASTER_TRANSMITTER_MODE_SELECTED))
  {
    if(count_wdt++>10)goto start;
  }
  // bit Co = 0, D/C# = 0
  I2C->DR = COMMAND_MODE;      
  while (!I2C_CheckEvent(I2C_EVENT_MASTER_BYTE_TRANSMITTING));   
  I2C->DR = cmd;              
  while (!I2C_CheckEvent(I2C_EVENT_MASTER_BYTE_TRANSMITTING));
  I2C->CR2 |= I2C_CR2_STOP;  
}
